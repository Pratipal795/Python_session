#Iterate 10 to 0 using for loop, do the same using while loop.
#using for loop
print("iterate using for loop :")
for i in range(10,-1,-1):
    print(i,end=" ")
print()
print()
print()

#using while loop
i=10
a=0
print("iterate using while loop :")
while i>=a:
    print(i,end=" ")
    i-=1
